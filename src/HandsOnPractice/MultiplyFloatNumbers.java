//Java Program to Multiply Two Floating Numbers

public class MultiplyFloatNumbers {
    public static void main(String[] args) {

        float f1 = (float) Math.random()*100;
        float f2 = (float) Math.random()*100;
        System.out.println("The multiplication of "+f1+" and "+f2+" is "+(f1*f2));

    }
}
